
<?php 

	//  La fonction DireBonjour($nom) ne fait rien que d'afficher du texte. Elle prend en params d'entrée un nom

	function DireBonjour($nom)
	{
		// corps de la fonction
		echo 'Bonjour ' . $nom . '! <br>';

	}

	// Appels de fonction avec différents params d'entrée.
	DireBonjour('Joseph');
	DireBonjour('Adèle');
	DireBonjour('Emilienne');
	DireBonjour('Natacha');
	DireBonjour('Patrick');
	DireBonjour('Dieu');
	DireBonjour('Esprit Infini');



 ?>